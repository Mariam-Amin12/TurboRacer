;THE FUNCTIONALITY OF THIS CODE IS RESETING THE COLORS OF PIXELS BEFORE DRAWING THE CAR


PUBLIC RESETCAR1
PUBLIC RESETCAR2
EXTRN HEIGHT1:WORD
EXTRN WIDTH1:WORD
EXTRN CENTER1:WORD
EXTRN STATE1:BYTE
EXTRN HEIGHT2:WORD
EXTRN WIDTH2:WORD
EXTRN CENTER2:WORD
EXTRN STATE2:BYTE
EXTRN PIXELS1:BYTE
EXTRN PIXELS2:BYTE
EXTRN TOP1:WORD
EXTRN TOP2:WORD
.MODEL SMALL
.STACK 64
.CODE
RESETCAR1 PROC FAR
CMP STATE1 , 0      
JNZ RIGHT
MOV BX,TOP1
PUSH BX

MOV AX,BX
MOV DX,HEIGHT1  
MOV SI,OFFSET PIXELS1

UPROW:
MOV DI,AX
MOV CX,WIDTH1


LOOP1:
MOV AL,[SI]
STOSB
INC SI
LOOP LOOP1

POP AX
ADD AX,320      ; MOVE TO THE NEXT ROW
PUSH AX
DEC DX          
CMP DX , 0
JNZ UPROW
POP AX
JMP EXIT



RIGHT:
CMP STATE1 , 1
JNZ LEFT
MOV BX,TOP1
PUSH BX

MOV AX,BX

MOV DX,HEIGHT1
MOV SI,OFFSET PIXELS1


RIGHTCOLUMN:
MOV DI,AX
MOV CX,WIDTH1

LOOP2:
MOV AL,[SI]
PUSH DI
STOSB
POP DI
INC SI
ADD DI,320
LOOP LOOP2
POP AX
SUB AX,1
PUSH AX
DEC DX
CMP DX , 0
JNZ RIGHTCOLUMN
POP AX
JMP EXIT



LEFT:
CMP STATE1,2
JNZ DOWN

MOV BX,TOP1
PUSH BX


MOV AX,BX
MOV DX,HEIGHT1
MOV SI,OFFSET PIXELS1


LEFTCOLUMN:
MOV DI,AX
MOV CX,WIDTH1

LOOP3: 
MOV AL,[SI]
PUSH DI
STOSB
POP DI
INC SI
SUB DI,320
LOOP LOOP3
POP AX
ADD AX,1
PUSH AX
DEC DX
CMP DX , 0
JNZ LEFTCOLUMN
POP AX
JMP EXIT



DOWN:
MOV BX,TOP1
PUSH BX

MOV AX,BX
MOV DX,HEIGHT1
MOV SI,OFFSET PIXELS1

DOWNROW:
MOV DI,AX
MOV CX,WIDTH1

LOOP4:
MOV AL,[SI]
STOSB
SUB DI,2
INC SI
LOOP LOOP4
POP AX
SUB AX,320
PUSH AX
DEC DX
CMP DX , 0
JNZ DOWNROW
POP AX
EXIT:
RET
RESETCAR1 ENDP






